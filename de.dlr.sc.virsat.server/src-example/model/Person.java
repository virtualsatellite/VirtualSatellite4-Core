package de.dlr.virsat.server.example.model;


import jakarta.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class Person {
	
    private String name = "John";

    public Person() {
    }
    
    public Person(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}