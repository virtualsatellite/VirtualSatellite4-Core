package de.dlr.virsat.server.example.model;

import jakarta.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class Hello {
	
	public Hello() {
		this.name = "John";
	}
	
	public Hello(String name) {
		this.name = name;
	}
	
	private String name;
	private String id;
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
